// 輔助函數：根據技能ID和等級獲取效果
function getSkillEffects(skillId, level) {
    const skill = SKILL_DATABASE[skillId];
    if (!skill) return null;

    const effects = skill.levels[level];
    if (!effects) return null;

    return {
        name: skill.name,
        category: skill.category,
        level: level,
        effects: effects,
        icon: skill.icon
    };
}

// 輔助函數：根據技能名稱搜尋
function findSkillByName(skillName) {
    for (let skillId in SKILL_DATABASE) {
        if (SKILL_DATABASE[skillId].name === skillName) {
            return {
                skillId: skillId,
                ...SKILL_DATABASE[skillId]
            };
        }
    }
    return null;
}

// 輔助函數：獲取所有技能列表
function getAllSkills() {
    return Object.keys(SKILL_DATABASE).map(skillId => ({
        skillId,
        ...SKILL_DATABASE[skillId]
    }));
}

// 輔助函數：按分類獲取技能
function getSkillsByCategory(category) {
    return Object.keys(SKILL_DATABASE)
        .filter(skillId => SKILL_DATABASE[skillId].category === category)
        .map(skillId => ({
            skillId,
            ...SKILL_DATABASE[skillId]
        }));
}
